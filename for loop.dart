void main()  
{  int num = 0;  
    for(num; num<=10; num++)           //for loop to print 1-10 numbers  
    {  
        print(num);     //to print the number  
    }  
    int a = 10;  
    for(a; a>=0; a--)           //for loop to print 1-10 numbers  
    {  
        print(a);     //to print the number  
    }  
}  